SELECT
    C.CAR_ID,
    C.CAR_TYPE,
    FLOOR(C.DAILY_FEE * 30 * (100 - P.DISCOUNT_RATE) / 100) AS FEE
FROM CAR_RENTAL_COMPANY_CAR C
LEFT JOIN CAR_RENTAL_COMPANY_RENTAL_HISTORY H
ON C.CAR_ID = H.CAR_ID AND H.CAR_ID IS NULL
JOIN CAR_RENTAL_COMPANY_DISCOUNT_PLAN P
ON C.CAR_TYPE = P.CAR_TYPE AND DURATION_TYPE = '30일 이상'
WHERE NOT EXISTS (
    SELECT 1
    FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY H
    WHERE C.CAR_ID = H.CAR_ID
    AND H.START_DATE <= '2022-11-30' AND H.END_DATE >= '2022-11-01'
    )
AND C.CAR_TYPE IN ('세단', 'SUV')
AND C.DAILY_FEE * 30 * (100 - P.DISCOUNT_RATE) / 100 >= 500000
AND C.DAILY_FEE * 30 * (100 - P.DISCOUNT_RATE) / 100 < 2000000
ORDER BY FEE DESC, C.CAR_TYPE, C.CAR_ID DESC;